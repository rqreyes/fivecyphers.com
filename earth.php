          <h4>Developing Texture</h4>
          <p>When I say texture, I refer to the different ways that you can move in a set. This encompasses level changing, slides, spins, moving both ways, jumping, vibrating, tempo changes—anything that will break up the monotony of a generic set. Consider a landscape—while a flat landscape with nothing but dirt is beautiful in its own right, think of how much more interesting a view is with texture, be it mountains, forest, or ocean.</p>
          <p><strong>Take Downs</strong> – You must first learn all the basic take downs and learn how to do them well. Once you’ve developed a strong vocabulary, develop unique take downs. These should have a focus on tempo, strength, explosiveness, or finesse.</p>
          <p><strong>Level Changes</strong> – Level changes are an excellent way to add diversity to your rounds. Moving up and down in power moves, doing level changes with your foot and floor works, and doing freezes on multiple levels adds visual diversity to what you are doing.</p>
          <p><strong>Jumps</strong> – Doing airborne moves is a very dynamic form of texture. Flips, powermoves in the air, and hops are one way of doing this, but consider simply doing a move you already know, and making it airborne.</p>
          <p><strong>Traveling Moves</strong> – Slides are a good way to travel across the floor, but consider that you can do any move as a traveling move. Powermoves, footwork, even freezes can travel. Take moves that you’ve already developed and figure out a way to move in different directions with them.</p>
          <p><strong>Moves Both Ways</strong> – It is important to learn to do moves both ways. I highly encourage you to learn your footwork and at least basic power and freezes on both sides. From there, be intentional about developing movement both ways in your sets. If you’re constantly rotating in one direction, you’ll begin to look monotonous.</p>

          <h4>Basic Set Formulas</h4>
          <p><strong>Toprock-Footwork-Freeze</strong> – The most fundamental of the sets. By doing tops, footwork, and ending in a freeze, you’ll put together a complete set. This formula is difficult to beat, has the necessary pieces for a full set, and will look good.</p> 
          <p><strong>Toprock-Power-Footwork-Freeze</strong> – One of the hardest sets to beat because it has all the pieces. You’ll be surprised at how well you can do at a jam by using this formula—Issei from Foundnation won R-16 multiple times just by using this single method of constructing sets, and Victor from The Squadron/MF Kidz often employs this formula.</p> 
          <p><strong>Toprock-Footwork-Power-Freeze</strong> – This formula is similar to the above in that it has all the pieces and is hard to beat, but it tends to have a bit more texture because you’ll need to figure out different ways to get into power.</p>
          <p><strong>Toprock-Power-Freeze</strong> – The basic "powermover" formula. This is extremely effective in crew battles, but tends to be difficult to win in formats with fewer people (i.e. 1v1s, 2v2s). Be aware that if you use this, there are many judges that may mark you down because you didn’t do footwork and "that’s not bboying/bgirling" (or so they would say). If this formula speaks to you, I encourage you to push the limits and make it as effective as possible.</p>
          <p><strong>Toprock-Freeze-Footwork-Freeze</strong> – A set with more "texture." While still relatively basic in terms of on-paper construction, this is a formula that can be hard to make flow. Play with this and learn how to incorporate different weapons in different parts of your dance.</p>

          <h4>On Speed in Bboying/Bgirling</h4>
          <p>Many bboys/bgirls assert that the faster you do moves, the better— even these bboys/bgirls understand that there are tempo changes, but the very fact of speed must be treated differently than just a matter of doing the moves.</p>
          <p>When in dancing, you may have tried to toprock or do footwork too quickly (or slowly), only to realize that you are not in rhythm. Likewise, in your day to day life you may have encountered instances where you were short on time and rushing through tasks, or days when you felt slower than normal. The emphasis in a discussion of “speed” is to go at your own pace and do things at your own rhythm. As I discussed in the Water Book, everything has its own rhythm—doing well, doing poorly, winning, losing. When you are out of these rhythms, you’ll experience fastness or slowness.</p>
